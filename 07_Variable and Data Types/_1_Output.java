public class _1_Output {
    public static void main(String arg[]) {
        // Outputs any kind of data that is stored into the variable location.
        System.out.println("Hey, Welcome to the final journey of the coding.");

        // print vs println

        // print doesn't give a line break so whenever you'll write anything from the
        // next time it will behave as concatenation to the previous o/p statement.
        System.out.print("Hellow ");
        System.out.print("World!");

        // Printing two blank lines
        System.out.println();
        System.out.println();
        
        // println gives a line break after showcasing the output.
        System.out.println("Hello World");
        System.out.println(", Welcome!");

        // \n -> It'll print all the next data after \n in next line.
        System.out.print("Hello,\n Sachin Kumar!");

    }
}